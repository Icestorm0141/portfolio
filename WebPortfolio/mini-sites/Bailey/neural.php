<h1 class="smallHeadline">Neural
  Network Modeling of Various Energy Intensive Systems for
  Prediction and Pattern Recognition Purposes<br />
  (1995&ndash; Present)  </h1>
<p class="content">The
  objectives of this ongoing research involve creating neural
  network models that solve classification and regression
  engineering problems. The objective is divided into two general
  tasks. The first involves the solution of an inverse problem.
  Given a robust set of system operational data, what can be learned
  from detailed data analysis in regards to the health and
  performance of the system? For example, can actual equipment
  operating data be used to reliably determine the equipment&rsquo;s
  current operating state? Secondly, can a neural network model be
  used to solve this inverse problem and can it be generalized over
  a class of systems. Application areas involve systems ranging from
  the City of Rochester&rsquo;s municipal buildings (50+ buildings)
  to an individual building&rsquo;s cooling plant.  </p>
