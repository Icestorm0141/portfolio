      <h2 style="margin-left: 0pt; margin-top: 0pt; margin-bottom: 6pt" class="smallHeadline"> Senior Capstone Design Advising  </h2>
      <p class="content">In order to adequately internalize the engineering design process while understanding the importance of effective team work in a multi-disciplinary context, engineering students (BS) at both RIT and USMA immerse themselves within a lengthy multidisciplinary team-based design experience as a culmination of their undergraduate programs. Therefore since 1998, I have had the opportunity to mentor fourteen multi-disciplinary teams throughout this experience. In order to provide the necessary mentoring for these individuals, I meet with the entire team once per week and with team leaders more frequently as required. By design, the projects often fall within my areas of strong interest from a technical and educational research perspective, such as within advanced thermodynamics; energy demand and supply strategies; educational training aides for use in classroom settings; and experimental set-ups involving energy-related apparatus. These areas are of interest to undergraduate students as well.  </p>
