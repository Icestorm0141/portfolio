                <p class="content">I have been fortunate to work with three exceptional graduate students over the past two years at RIT, two of whom have already defended their research. Since beginning their research experiences at RIT, I have witnessed marked growth in the BS/MS graduate students&rsquo; intellectual curiosity and self-confidence. One of my past BS/MS students has recently begun pursuing her PhD in engineering at Berkeley with the hopes of beginning an academic career after graduation. Hopefully, my influence on her during her BS/MS program had an impact in this decision.</p>
