 <p class="smallHeadline">Course, Laboratory, and Program Development</p>
        <p>Developing curriculum is an activity that holds great interest for me for several reasons. The experiences afford me the opportunity to become better acquainted with a topic while creatively devising ways in which to present material which will enhance student learning. In addition, many of my past curricular designs have occurred within the context of a team of academics and have been positive experiences. The courses/programs/laboratories that have resulted from most of these teams have been extremely successful based on student and professor feedback.</p>
        <p> An example of a current team based approach to course redesign is underway within the KGCOE Honors Program. Over the past six months, I have been <span style="text-decoration:underline">leading</span> an effort by the College Honors Committee to redesign our existing 1<sup>st</sup> and 2<sup>nd</sup> year honors course sequence (six courses) in order to improve the alignment of the course learning objectives with the college&rsquo;s educational mission; <em>product innovation in a global economy.</em> Using the results of existing course assessment; benchmarks against other University Honors Programs; and focus group feedback from current RIT Honors students, the team has developed new course learning objectives; approximately fifty new lessons; and several new design projects and exercises (refer to Teaching Activities Notebook, Tab A-7).</p>  
<p>This coming fall we are offering two of the six redesigned courses for the 1<sup>st</sup> and 2<sup>nd</sup> year Honors students. For each course, students will be presented with clear learning objectives, detailed lists of lesson topics/speakers, and understandable links between the course and the overall honors program. Ideally, students within the KGCOE Honors Program will benefit directly from these course enhancements, and annual course assessment will check the validity of this premise. </p> 
<p>Within the next year, several of the Honors Committee members, including myself and Dean Palmer, will be authoring an educational conference paper on the course sequence redesign process as well as the assessment results. Publishing, presenting, and disseminating this information will enhance the reputation of RIT. n the past, I have published several journal articles and conference proceedings documenting my course development activities (refer to Publications Notebook papers C2, C3, C4, D3, 4, D5, D6, D8) and will do the same with this current design activity.</p>
