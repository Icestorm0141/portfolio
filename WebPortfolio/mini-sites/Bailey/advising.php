        <p class="content">I
      have been fortunate to work with three exceptional graduate
      students over the past two years at RIT, two of whom have already
      defended their research. Since beginning their research
      experiences at RIT, I have witnessed marked growth in the BS/MS
      graduate students&rsquo; intellectual curiosity and
      self-confidence. One of my past BS/MS students has recently begun
      pursuing her PhD in engineering at Berkeley with the hopes of
      beginning an academic career after graduation. Hopefully, my
      influence on her during her BS/MS program had an impact in this
      decision.<br />
      <br />
      At least once per week, I meet with my graduate
      students to guide their efforts and answer questions, etc.
      However, my goal is for them to become more self-directed and
      motivated throughout the process of completing the MS
      requirements. I also attempt to immerse the BS/MS students within
      the graduate school experience by holding joint weekly meeting and
      creating opportunities for discussions on technical issues related
      to their research. Our joint objective by the end of each
      student&rsquo;s graduate experience is for the student to author
      two technical publications; one suitable for conference
      proceedings and the other journal quality. Whenever possible, the
      conference paper is presented by the student at the technical
      meeting. The following publications have resulted from these
      activities (BS/MS student names in bold):<br />
