  </li>
  <li>
    <p class="content">1991-1993:
      Facilities Engineer / Project Manager, Fordham University, New
      York, NY. Responsible for design, contract administration, and
      construction supervision for all campus mechanical projects,
      including chiller replacement, energy management system upgrade
      and installations, re-commissioning of building mechanical
      system, etc. Performed project management duties including
      contract bidding and negotiations on several large projects with
      budgets in excess of $1million. Prepared RFP's; interviewed and
      supervised professional consultants; assisted in determining
      priorities and estimated costs for future capital projects;
      building and ADA code compliance.  </p>
  </li>
  <li>
    <p class="content">1990-1991:
      Associate Design Engineer, Einhorn Yaffee Prescott Architecture &amp;
      Engineering, P.C., Albany, NY. Designed mechanical systems for
      commercial buildings; performed building cooling and heating load
      analysis; produced contract documents; reviewed shop submittals,
      conducted site visits and inspections. Worked extensively with
      energy codes and utility rebate programs.  </p>
  </li>
  <li>
    <p>1988-1990: Associate
      Facilities Engineer, IBM Corporation, Kingston, NY. Assistant
      project manager on $20 million building abatement and retrofit
      project. Duties included design review, progress report
      submission, site inspections, budget analysis, etc. Prepared
      preliminary design and presented a financial business case
      involving the proposed construction of a $4 million elevated
      pedestrian walkway on the IBM Kingston site which was
      subsequently constructed. Designed HVAC, piping, and fire
      protection systems for computer areas, office centers and
      computer test cells.  </p>
