<p class="smallHeadline"><em>Initiatives</em></p>
<p class="content">In
  order to establish a diverse learning environment within our
  college in regards to gender, a systematic approach is required to
  address the following essential elements:</p>
<ol><li>Establish
  and expand the pipeline of women interested in pursuing
  engineering majors within RIT</li>
<li>Improve
  RIT&rsquo;s ability to attract and recruit women engineering
  students and faculty to its engineering programs</li>
  <li>Retain
  RIT women engineering students and faculty</li></ol>
<p class="content">In
  the months after I started my current position at RIT (Fall 2003)
  as the Kate Gleason Endowed Chair, I began creating the framework
  for this systematic approach while building momentum among faculty
  and staff around the issue. Within the Teaching Activities
  Notebook under Tab A-6 is a series of documents that I have
  created to assist in the development of this overall program. </p>
<p class="content">Throughout
  the past two years, I have learned many valuable lessons regarding
  how to develop a program with limited monetary resources. I found
  that the most effective way to get people involved in efforts to
  improve the college&rsquo;s gender diversity is to provide
  volunteer opportunities for those interested which are
  well-bounded and therefore reasonable to undertake. Volunteers
  need direction and because our volunteers are all busy people,
  their time is very valuable. It is imperative that time spent in
  support of WE@RIT initiatives is not perceived as excessive or
  wasteful. Therefore, planning meetings are kept up-beat, timely,
  and well organized. In order to provide the direction required to
  grow this program, I believe that I have shown the necessary
  leadership to do so. </p>
<p class="content">The
  direction that WE@RIT follows needs to be tied explicitly to the
  mission of the overall program. Therefore, we created a mission
  for WE@RIT which is to <em>establish and enhance a &ldquo;pipeline&rdquo; <br/>
    of pre-college and college programs for women that will
    ultimately lead them to the engineering workforce</em>. I crafted a
  clear multi-year strategic plan that is directly tied to this
  mission and includes several facets including
  outreach/recruitment, retention, University advocacy, research,
  and industrial partnering. </p>
  <p class="content">During
  my first year at RIT, a student coop and I created a new outreach
  pre-engineering program designed for girls in 7<sup>th</sup> and
  8<sup>th</sup> grade. It evolved into the 2.5 day Park &amp; Ride
  Program: Amusement Park Design which has now been held twice with
  a new focus on 6<sup>th</sup> and 7<sup>th</sup> graders (refer to
  Tab A-8 in Teaching Activities Notebook). Prior to Park &amp;
  Ride, KGCOE had one outreach initiative, the SWE Annual Sleepover
  and Shadow Event which is run by our Society of Women (SWE)
  Student Section. It is a highly successful, pre-engineering
  two-day program with an audience of 11<sup>th</sup> grade women.  </p>
  <p class="content">The
  Park &amp; Ride event was the first attempt by WE@RIT to initiate
  an outreach event and many lessons were learned throughout the
  process. It also provided an excellent means by which to grow the
  momentum of the WE@RIT program among the RIT community. Since
  then, with the assistance of another student coop and a dedicated
  core of faculty and staff volunteers, we have created an outreach
  event titled &ldquo;Expanding Your Horizons&rdquo; or EYH which is
  a one day event with an audience of 8<sup>th</sup>-10<sup>th</sup> graders (refer to Tab A-7 in Teaching Activities Notebook).
  Several other events and programs have been designed and launched
  over the past two years, including a climate assessment plan
  (refer to Tab A-10 in Teaching Activities Notebook) and a unique
  course that I am designing with colleagues from ME and the College
  of Liberal Arts. The new course is for 1<sup>st</sup> and 2<sup>nd</sup> year women engineering students and is titled &ldquo;Women
  Engineering Their Futures&rdquo; (refer to Tab A-2 in Teaching
  Activities Notebook).  </p>
<p class="content">Over
  the course of the past two years, the framework for WE@RIT has
  been developed including a mission statement, strategic plan, and
  series of integrated events and programs all of which support the
  mission. In addition, a small staff including student hires and a
  half-time coordinator have been hired, a physical space has been
  dedicated, and a comprehensive governance structure has been
  developed. This structure includes an Executive Board, initiated
  in June 2005 and consisting of faculty, academic administrators
  and industrial representatives, which support strategic growth of
  the WE@RIT Program and its associated initiatives through
  quarterly roundtable discussions.  </p>
<p class="content">In
  addition to all of the above mentioned WE@RIT activities, in June
  and July 2005, I had the opportunity to lead a large group of
  academics from KGCOE, CAST, COL, CAST, and GCCIS during the
  preparation of an NSF ADVANCE Institutional Transformation grant
  proposal. This NSF program is aimed at increasing the number of
  women faculty within science and engineering academe. Our proposed
  project &ldquo;Promotion and Leadership for Academic Careers in
  Engineering and Science</p>
<p class="content">(PLACES
  @ RIT)&rdquo; addresses the ADVANCE program goal directly through
  an integrated series of innovative efforts. Although leading and
  actively participating in the concept creation and proposal
  writing processes were extremely time consuming, the overall
  experience was one of the most positive that I have encountered
  thus far during my academic career. The team was diverse based on
  gender and academic background and the cohesiveness and subsequent
  outcome were exceptional. A complete copy of the proposal is
  included within the Proposal Notebook.  </p>
