<h1 class="smallHeadline">Exergetic
  Thermodynamic Analysis of Complex, Energy Intensive Systems<br />
(2002&ndash; Present)  </h1>
<p class="content">In
  the design and operation of energy intensive systems, the
  possibility of improving a system&rsquo;s operational efficiency
  is essential to minimize energy consumption and reduce negative
  environmental impacts. There are various methods available to
  improve the performance of an existing complex, energy intensive
  system, however many involve trail and error approaches with
  unpredictable results. Analytical approaches based on
  thermodynamic analysis and optimizations are also available to
  improve system efficiency and performance. My students and I
  continue to study, expand, and generalize an analytical solution
  to this problem using novel thermodynamic approaches involving
  exergy flow graphs and simulations created with a commercially
  available software package (EES). The approach allows the
  determination of exergy losses and exergetic efficiency of the
  total system. It also shows the relationship between the exergy
  efficiency of an individual element and that of the whole system.  </p>
